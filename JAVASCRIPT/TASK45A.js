// JAVASCRIPT IF ELSE STATEMENT
/* > greater than
< less than
=== equal to */

const bedc = 'light';

if (bedc === 'light') {
    console.log ("Get up to iron your clothes");
}else {
    console.log ("Sleep till morning");
}

let age = 35;

if (age === 25) {
    console.log ("Please do not touch the snooker board");
}else if (age === 35) {
    console.log ("Please go ahead and play snooker game for free");
}else {
    console.log ("Please play but at a price");
}

/* AND &&
OR || */

const birth1 = 60;
const birth2 = 70;

if (birth1 === 30 || birth2 === 40) {
    console.log ("My pleasure. welcome to the good life");
}else if (birth1 === 60 || birth2 === 70) {
    console.log ("Sorry, your knees are now feeble");
}else {
    console.log ("You are almost there");
}

const blink = 1991;
const blank = 1995;

if (blink === 1991 && blank === 1995) {
    console.log ("We are of the same generation");
}else {
    console.log ("We are Gen Z baddies");
}